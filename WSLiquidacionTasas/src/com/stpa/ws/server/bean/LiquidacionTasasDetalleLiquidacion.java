package com.stpa.ws.server.bean;

public class LiquidacionTasasDetalleLiquidacion {
	private String id = "";
	private String codTarifa = "";
	private String numUnidades = "";
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getCodTarifa() {
		return codTarifa;
	}
	public void setCodTarifa(String codTarifa) {
		this.codTarifa = codTarifa;
	}
	public String getNumUnidades() {
		return numUnidades;
	}
	public void setNumUnidades(String numUnidades) {
		this.numUnidades = numUnidades;
	}
	
}
