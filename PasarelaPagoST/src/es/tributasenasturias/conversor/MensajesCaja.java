package es.tributasenasturias.conversor;

public class MensajesCaja {
	private String codigoCaja;
	private String codigoEpst;
	private String textoEpst;
	/**
	 * @return the codigoCaja
	 */
	public String getCodigoCaja() {
		return codigoCaja;
	}
	/**
	 * @param codigoCaja the codigoCaja to set
	 */
	public void setCodigoCaja(String codigoCaja) {
		this.codigoCaja = codigoCaja;
	}
	/**
	 * @return the codigoEpst
	 */
	public String getCodigoEpst() {
		return codigoEpst;
	}
	/**
	 * @param codigoEpst the codigoEpst to set
	 */
	public void setCodigoEpst(String codigoEpst) {
		this.codigoEpst = codigoEpst;
	}
	/**
	 * @return the textoEpst
	 */
	public String getTextoEpst() {
		return textoEpst;
	}
	/**
	 * @param textoEpst the textoEpst to set
	 */
	public void setTextoEpst(String textoEpst) {
		this.textoEpst = textoEpst;
	}
	
}
