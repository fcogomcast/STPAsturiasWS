package es.tributasenasturias.conversor;

public class MensajesBD {
	private String codigoBD;
	private String codigoServicio;
	private String textoServicio;
	/**
	 * @return the codigoBD
	 */
	public String getCodigoBD() {
		return codigoBD;
	}
	/**
	 * @param codigoBD the codigoBD to set
	 */
	public void setCodigoBD(String codigoBD) {
		this.codigoBD = codigoBD;
	}
	/**
	 * @return the codigoServicio
	 */
	public String getCodigoServicio() {
		return codigoServicio;
	}
	/**
	 * @param codigoServicio the codigoServicio to set
	 */
	public void setCodigoServicio(String codigoServicio) {
		this.codigoServicio = codigoServicio;
	}
	/**
	 * @return the textoServicio
	 */
	public String getTextoServicio() {
		return textoServicio;
	}
	/**
	 * @param textoServicio the textoServicio to set
	 */
	public void setTextoServicio(String textoServicio) {
		this.textoServicio = textoServicio;
	}
	
}
