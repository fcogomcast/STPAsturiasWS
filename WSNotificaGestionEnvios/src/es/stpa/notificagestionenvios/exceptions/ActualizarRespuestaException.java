package es.stpa.notificagestionenvios.exceptions;

public class ActualizarRespuestaException extends Exception{

	

	/**
	 * 
	 */
	private static final long serialVersionUID = -6612614036334760519L;

	public ActualizarRespuestaException(String message, Throwable cause) {
		super(message, cause);
	}

	public ActualizarRespuestaException(String message) {
		super(message);
	}

	public ActualizarRespuestaException(Throwable cause) {
		super(cause);
	}

}
