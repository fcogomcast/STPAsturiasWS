package com.stpa.ws.server.bean;

public class NumAutoliquidacionPeticion {
	private String p_cliente = "";
	private String p_timestamp = "";
	private String p_modelo_autoliquidacion = "";
	private String p_libre = "";
	private String p_servicio = "";
	
	public String getP_cliente() {
		return p_cliente;
	}
	public void setP_cliente(String p_cliente) {
		this.p_cliente = p_cliente;
	}
	public String getP_timestamp() {
		return p_timestamp;
	}
	public void setP_timestamp(String p_timestamp) {
		this.p_timestamp = p_timestamp;
	}
	public String getP_modelo_autoliquidacion() {
		return p_modelo_autoliquidacion;
	}
	public void setP_modelo_autoliquidacion(String p_modelo_autoliquidacion) {
		this.p_modelo_autoliquidacion = p_modelo_autoliquidacion;
	}
	public String getP_libre() {
		return p_libre;
	}
	public void setP_libre(String p_libre) {
		this.p_libre = p_libre;
	}
	public String getP_servicio() {
		return p_servicio;
	}
	public void setP_servicio(String p_servicio) {
		this.p_servicio = p_servicio;
	}
}
