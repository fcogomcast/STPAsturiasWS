package com.stpa.ws.server.bean;

public class NumAutoliquidacionRespuesta {
	private String timestamp = "";
	private String resultado = "";
	private String numero_autoliquidacion = "";
	
	public String getTimestamp() {
		return timestamp;
	}
	public void setTimestamp(String timestamp) {
		this.timestamp = timestamp;
	}
	public String getResultado() {
		return resultado;
	}
	public void setResultado(String resultado) {
		this.resultado = resultado;
	}
	public String getNumero_autoliquidacion() {
		return numero_autoliquidacion;
	}
	public void setNumero_autoliquidacion(String numero_autoliquidacion) {
		this.numero_autoliquidacion = numero_autoliquidacion;
	}
}
